def test_project_api_import_project_data_resets_backend_session(tmp_path: Path) -> None:
    client = TestClient(app)
    project = _constant_write_project(tmp_path)
    project.project_name = "Imported From Browser"
    project.settings.project_path = None

    response = client.post("/api/projects/import", json={"project": project.model_dump(mode="json")})

    assert response.status_code == 200
    imported = response.json()
    assert imported["project_name"] == "Imported From Browser"
    assert imported["graph"]["nodes"]["Write1"]["params"]["path"].endswith("render.####.png")
    assert imported["settings"]["project_path"] is None


def test_project_save_prefers_top_level_active_graph_payload(tmp_path: Path) -> None:
    project = _constant_write_project(tmp_path)
    project.script_tabs[0].graph = project.graph.model_copy(deep=True)
    project.script_tabs[0].graph.nodes["Write1"].params["path"] = str(tmp_path / "stale.####.png")
    project.graph.nodes["Write1"].params["path"] = str(tmp_path / "fresh.####.png")

    output_path = save_project_file(project, tmp_path / "fresh_graph")
    loaded = load_project_file(output_path)

    assert loaded.graph.nodes["Write1"].params["path"].endswith("fresh.####.png")
    assert loaded.script_tabs[0].graph.nodes["Write1"].params["path"].endswith("fresh.####.png")


def test_nuke_export_writes_stack_connections_for_merge(tmp_path: Path) -> None:
    graph = ProjectGraph(
        nodes={
            "ConstantA": Node(id="ConstantA", type="Constant", position=(0, 0), params={"r": 1.0, "a": 1.0}),
            "ConstantB": Node(id="ConstantB", type="Constant", position=(200, 0), params={"b": 1.0, "a": 1.0}),
            "Merge1": Node(id="Merge1", type="Merge", position=(100, 120), params={"operation": "add", "mix": 0.5}),
            "Viewer1": Node(id="Viewer1", type="Viewer", position=(100, 240), params={"active_input": "0"}),
        },
        edges=[
            Edge(id="a-merge", source_node="ConstantA", target_node="Merge1", target_socket="a"),
            Edge(id="b-merge", source_node="ConstantB", target_node="Merge1", target_socket="b"),
            Edge(id="merge-viewer", source_node="Merge1", target_node="Viewer1", target_socket="0"),
        ],
    )
    project = _project_from_graph(graph)

    nuke_text = build_nuke_script(project, tmp_path / "merge_test.nk")

    assert "Merge2 {" in nuke_text
    assert " operation plus" in nuke_text
    assert " mix 0.5" in nuke_text
    assert "push $OC_ConstantB\npush $OC_ConstantA\nMerge2 {" in nuke_text
    assert "set OC_Merge1 [stack 0]" in nuke_text


def test_nuke_export_uses_noop_for_unsupported_nodes(tmp_path: Path) -> None:
    graph = ProjectGraph(
        nodes={
            "Constant1": Node(id="Constant1", type="Constant", position=(0, 0), params={"a": 1.0}),
            "FutureNode1": Node(id="FutureNode1", type="FutureNode", position=(0, 120), params={"strength": 2}),
        },
        edges=[Edge(id="constant-future", source_node="Constant1", target_node="FutureNode1")],
    )
    project = _project_from_graph(graph)

    nuke_text = build_nuke_script(project, tmp_path / "unsupported_test.nk")

    assert "NoOp {" in nuke_text
    assert 'label "OpenComp unsupported node: FutureNode' in nuke_text
    assert "push $OC_Constant1\nNoOp {" in nuke_text


