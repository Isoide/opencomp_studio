from __future__ import annotations

from pathlib import Path

from fastapi.testclient import TestClient

from opencomp.app import app
from opencomp.cli import main, parse_frame_range
from opencomp.core.models import Edge, Node, Project, ProjectGraph, ProjectSettings, ScriptTab
from opencomp.core.project_io import load_project_file, save_project_file
from opencomp.io.nuke_exporter import build_nuke_script


def test_parse_frame_range_supports_single_ranges_lists_and_steps() -> None:
    assert parse_frame_range("1001", 1, 1) == [1001]
    assert parse_frame_range("1001-1003", 1, 1) == [1001, 1002, 1003]
    assert parse_frame_range("1001,1003,1005", 1, 1) == [1001, 1003, 1005]
    assert parse_frame_range("1001-1006x2", 1, 1) == [1001, 1003, 1005]
    assert parse_frame_range(None, 1008, 1010) == [1008]


def test_project_api_save_load_and_export_nuke_script(tmp_path: Path) -> None:
    client = TestClient(app)
    project = client.post("/api/projects/new").json()
    project["project_name"] = "Disk Test"

    save_response = client.post("/api/projects/save", json={"path": str(tmp_path / "disk_test"), "project": project})
    assert save_response.status_code == 200
    saved = save_response.json()
    assert saved["settings"]["project_path"].endswith(".opencomp")
    assert (tmp_path / "disk_test.opencomp").exists()

    load_response = client.post("/api/projects/load", json={"path": str(tmp_path / "disk_test.opencomp")})
    assert load_response.status_code == 200
    assert load_response.json()["project_name"] == "Disk Test"

    export_response = client.post("/api/projects/export-nuke", json={"path": str(tmp_path / "disk_test.nk")})
    assert export_response.status_code == 200
    assert export_response.json()["status"] == "exported"
    nuke_text = (tmp_path / "disk_test.nk").read_text(encoding="utf-8")
    assert "version 16.0 v6" in nuke_text
    assert "Root {" in nuke_text
    assert "Read {" in nuke_text
    assert "Grade {" in nuke_text
    assert "Viewer {" in nuke_text

    content_response = client.post("/api/projects/export-nuke/content", json={"path": "browser_export.nk"})
    assert content_response.status_code == 200
    assert "Generated by OpenComp Studio Nuke exporter v1" in content_response.text
    assert "Viewer {" in content_response.text


