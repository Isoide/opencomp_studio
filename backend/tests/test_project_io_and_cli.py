def test_cli_can_run_script_and_save_project(tmp_path: Path) -> None:
    project = _constant_write_project(tmp_path)
    project_path = save_project_file(project, tmp_path / "script_project")
    saved_path = tmp_path / "script_project_updated.opencomp"

    exit_code = main(
        [
            str(project_path),
            "--eval",
            "opencomp.node('root').value('name').setValue('CLI Edited')",
            "--save",
            str(saved_path),
            "--export-nuke",
            str(tmp_path / "script_project_updated.nk"),
        ]
    )

    assert exit_code == 0
    loaded = load_project_file(saved_path)
    assert loaded.project_name == "CLI Edited"
    assert "Generated by OpenComp Studio Nuke exporter v1" in (tmp_path / "script_project_updated.nk").read_text(
        encoding="utf-8"
    )


def _constant_write_project(tmp_path: Path) -> Project:
    graph = ProjectGraph(
        nodes={
            "Constant1": Node(
                id="Constant1",
                type="Constant",
                name="Constant",
                position=(80, 80),
                params={"width": 8, "height": 4, "r": 0.25, "g": 0.5, "b": 0.75, "a": 1.0},
            ),
            "Write1": Node(
                id="Write1",
                type="Write",
                name="Write",
                position=(80, 220),
                params={"path": str(tmp_path / "render.####.png"), "overwrite": True},
            ),
        },
        edges=[Edge(id="e-constant-write", source_node="Constant1", target_node="Write1")],
    )
    script = ScriptTab(id="main", name="Comp 1", graph=graph, kind="comp")
    return Project(
        project_name="CLI Test",
        settings=ProjectSettings(frame_start=1001, frame_end=1002, width=8, height=4),
        graph=graph,
        script_tabs=[script],
        active_script_id="main",
    )


def _project_from_graph(graph: ProjectGraph) -> Project:
    script = ScriptTab(id="main", name="Comp 1", graph=graph, kind="comp")
    return Project(
        project_name="Nuke Export Test",
        settings=ProjectSettings(frame_start=1001, frame_end=1010, width=1920, height=1080),
        graph=graph,
        script_tabs=[script],
        active_script_id="main",
    )
